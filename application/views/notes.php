<h1 class="susu">School Rules and Regulations
</h1>

<br>

<p class="soso">The school rules have been established in partnership with the community over a long period of time. They reflect the school community’s expectations in terms of acceptable standards of behaviour, dress and personal presentation in the widest sense. Students are expected to follow the school rules at all times when on the school grounds, representing the school, attending a school activity or when clearly associated with the school i.e. when wearing school uniform.</p>




 <br>

<p class="xde">Students have the responsibility:</p>

<div class="col-md-8">
  <div class="list-group">
