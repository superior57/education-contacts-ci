    <a href="#" class="list-group-item">To respect the right of others to learn</a>
    <a href="#" class="list-group-item">To respect their peers and teachers regardless of ethnicity, religion or gender</a>
    <a href="#" class="list-group-item">To respect the property and equipment of the school and others</a>
    <a href="#" class="list-group-item">To carry out reasonable instructions to the best of their ability</a>
    <a href="#" class="list-group-item">To conduct themselves in a courteous and appropriate manner in school and in public</a>
    <a href="#" class="list-group-item">To keep the school environment and the local community free from litter</a>
    <a href="#" class="list-group-item">To observe the uniform code of the school</a>
    <a href="#" class="list-group-item">To read all school notices and bring them to their parents’/guardians’ attention</a>
